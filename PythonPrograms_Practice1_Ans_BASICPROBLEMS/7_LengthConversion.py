# 7_LengthConversion.py

# Input: Enter length in centimeters
length_cm = float(input("Enter length in centimeters: "))

# Convert to meters and kilometers
length_m = length_cm / 100
length_km = length_cm / 100000

# Output: Display the conversions
print(f"Length in meters: {length_m}")
print(f"Length in kilometers: {length_km}")
